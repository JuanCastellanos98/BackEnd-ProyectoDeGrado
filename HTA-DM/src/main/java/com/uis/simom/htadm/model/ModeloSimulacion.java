	public void calcularRiesgo(int edad, int tiempo, int sexo, double altura, int herencia, int fumar, double calorias,
			double upDownCalorias, int upDown, double peso, int sbp, int dbp, int semanaC, int semanaF, int met1,
			int met2, int met3, int met4, int met5, int t1, int t2, int t3, int t4, int t5, int antecedentesDM2,
			int ali, int niv_glu, int med_HTA) {



		// Declaracion de parametros por sectores
		// Riesgo hta
		int S = sexo;
		double C_Se = -0.202933;
		double C_Ed = -0.156412;
		double C_SBP = -0.059330;
		double C_DBP = -0.128468;
		double C_BMI = -0.033881;
		double Alt = altura;
		double C_ExDBP = 0.001624;
		double C_Her = -0.166121;
		int op_He = herencia;
		int op_cig = fumar;
		double C_cig = -0.190731;
		double Beta = 22.949536;
		double scala = 0.876925;

		// Sector peso
		double Cal_Ex = upDownCalorias;
		int op_CalExt = upDown;
		double EI_d = calorias;
		double PAct = peso;
		int Con_C_Kg = 7700;

		// Presión arteria
		int op_SBP = sbp;
		int op_DBP = dbp;

		// Auxiliares
		// Semanas en la/las que el paciente prevé disminuir/aumentar la ingesta
		// calórica.
		int Mom_d = semanaC;
		// Semanas en el/los que el paciente prevé dejar/comenzar a fumar.
		int Mom_cc = semanaF;
		int Eact = edad;
		int ts = Eact;
		// *********************************************************************//

		// Declaracion de niveles
		// Auxiliares
		double Cont = 1;
		double Ed = Eact;
		double T_tra = ts * 365;
		double T_cig = 0;
		double P_dejaf = 0;

		// Sector peso
		double P = PAct;
		double EI = EI_d;
		// ********************************************************************//

		// Declaracion de auxiliares para el while
		double a = 1;
		double b = 365;
		double pasoSimu;
		pasoSimu = a / b;

		double tiempoI = Eact;
		double tiempoF = tiempo;
		double aux1 = 1;

		// Declaración de variables auxiliares
		// DM2
		double Riesgo_DM2 = 1;
		// AUXILIARES
		double Ed_c;
		double cig_c;
		double Mcc_d = (Mom_cc * 7) + 1;
		double Tempo = Mcc_d;
		// Presion arterial
		double Dif_p;
		// double c_ps;
		// double c_pd;
		double SBP;
		double DBP;
		// cigarrilo
		double P_cig;
		// double Pa_H;
		// double Pa_M;
		// Actividad fisica
		double BEE;
		double P_re;
		double P1;
		double P2;
		double P3;
		double P4;
		double P5;
		double PAL;
		// Sector peso
		double Dec_d;
		double T_com;
		double PA;
		double TEE;
		double EI_ade;
		double EB;
		// double N_H;
		// double N_M;
		double IMC = P / (Alt * Alt);
		// Riesgo HTA
		double Reg_Ed;
		double R_Ed;
		double R_se = C_Se * S;
		double R_Cig;
		double R_Her = op_He * C_Her;
		double R_ExDBP;
		double R_IMC;
		double R_DBP;
		double R_SBP;
		double Ec_HTA;
		double R_HTA;

		double Sum_F;

		// Declaracion de flujos
		// AUXILIARES
		
		double F_tt = 365;
		double F_dia;
		double Fs_tempo = 365;
		double F_pdef;
		// Sector peso
		double AC;
		double DC;
		double AP;
		double DP;
		int aux2 = 0;

		while (tiempoI <= tiempoF) {
			// auxiliares
			if (Mom_d == 0) {
				Ed_c = 10000;
			} else {
				Ed_c = (Eact * 365) + (Mom_d * 7);
			}
			if (Mom_cc == 0) {
				cig_c = 50000;
			} else {
				cig_c = (Eact * 365) + (Mom_cc * 7);
			}

			// presion arterial
			Dif_p = P - PAct;

			SBP = multSistolico(Dif_p) + op_SBP;
			DBP = multDiasistolico(Dif_p) + op_DBP;

			// Cigarrillo
			if (T_tra >= cig_c) {
				if (op_cig == 1) {
					if (S == 0) {
						if (T_cig <= 1825) {
							P_cig = (multPesoEdadH(T_cig)) * P_dejaf;
						} else {
							P_cig = 0.0939 * P_dejaf;
						}

					} else {
						if (T_cig <= 1825) {
							P_cig = (multPesoEdadM(T_cig)) * P_dejaf;

						} else {
							P_cig = 0.1354 * P_dejaf;
						}
					}
				} else {
					P_cig = 0;
				}
			} else {
				P_cig = 0;
			}
			// Actividad fisica
			P_re = P + P_cig;

			if (S == 0) {
				BEE = (293 - (3.8 * Ed) + (456.4 * Alt) + (10.12 * P_re));
			} else {
				BEE = (204 - (4 * Ed) + (450.5 * Alt) + (11.69 * P_re));
			}
			P1 = ((met1 - 1) * (((1.15 / 0.9) * t1) / 1440)) / (BEE / (0.0175 * 1440 * P_re));
			P2 = ((met2 - 1) * (((1.15 / 0.9) * t2) / 1440)) / (BEE / (0.0175 * 1440 * P_re));
			P3 = ((met3 - 1) * (((1.15 / 0.9) * t3) / 1440)) / (BEE / (0.0175 * 1440 * P_re));
			P4 = ((met4 - 1) * (((1.15 / 0.9) * t4) / 1440)) / (BEE / (0.0175 * 1440 * P_re));
			P5 = ((met5 - 1) * (((1.15 / 0.9) * t5) / 1440)) / (BEE / (0.0175 * 1440 * P_re));
			PAL = P1 + P2 + P3 + P4 + P5;
			// Sector peso
			if (T_tra <= Ed_c) {
				Dec_d = op_CalExt;
			} else {
				if (op_CalExt == 1) {
					Dec_d = 0;
				} else {
					Dec_d = 1;
				}
			}
			T_com = PAL;
			if (S == 0) {
				PA = multN_H(T_com);

			} else {
				PA = multN_M(T_com);

			}

			if (S == 0) {
				TEE = (864 - (9.72 * Ed) + (PA * (14.2 * P_re) + (503 * Alt)));
			} else {
				TEE = (387 - (7.31 * Ed) + (PA * (10.9 * P_re) + (660.7 * Alt)));
			}

			// RIESGO HTA
			Reg_Ed = Ed - (Eact - 20);
			if (S == 1) {
				R_Ed = ((C_Ed * (multRegPesoEdadH(Reg_Ed))) * Ed);
			} else {
				R_Ed = ((C_Ed * ((multRegPesoEdadM(Reg_Ed)))) * Ed);
			}
			if (T_tra >= cig_c) {
				if (op_cig == 1) {
					R_Cig = 0;
				} else {
					R_Cig = C_cig;
				}
			} else {
				R_Cig = op_cig * C_cig;
			}
			R_DBP = DBP * C_DBP;
			R_SBP = SBP * C_SBP;
			R_ExDBP = DBP * C_ExDBP * Ed;

			R_IMC = IMC * C_BMI;
			Sum_F = Beta + R_DBP + R_Her + R_Cig + R_IMC + R_ExDBP + R_SBP + R_Ed + R_se;
			Ec_HTA = (1 - Math.exp(-Math.exp((Math.log(1) - Sum_F) / scala))) * 100;
			if (Cont == 1) {
				if (Ec_HTA < 100) {
					R_HTA = Ec_HTA;
					this.listRiesgoHTA.add(R_HTA);
					this.listRiesgoDm2.add(Riesgo_DM2);
					this.listEdad.add(Ed);
					this.listPal.add(PAL);
					this.listImc.add(IMC);
					this.listPeso.add(P);
					//System.out.println("lEDAD= " + lEDAD + " lHTA= " + lHTA);
				} else {
					R_HTA = 100;
					this.listRiesgoHTA.add(R_HTA);
					this.listRiesgoDm2.add(Riesgo_DM2);
					this.listEdad.add(Ed);
					this.listPal.add(PAL);
					this.listImc.add(IMC);
					this.listPeso.add(P);
					// System. out. println(lEDAD);
				}
			} else {
				R_HTA = 0;
			}
			// Declaración de flujos
			// auxiliares
			if (aux1 <= 364) {
				// Cont=Cont+pasosimu;
				Cont = 0;
				aux1 = aux1 + 1;
			} else {
				Cont = 1;
				aux1 = 1;
			}

			if (T_tra >= (cig_c)) {
				F_dia = 365;
			} else {
				F_dia = 0;
			}
			if (R_Cig == 0) {
				if (op_cig == 1) {
					if (Tempo <= 1.5) {
						if (Tempo >= 0.4) {
							F_pdef = P * 365;
						} else {
							F_pdef = 0;
						}
					} else {
						F_pdef = 0;
					}
				} else {
					F_pdef = 0;
				}
			} else {
				F_pdef = 0;
			}
			if (Dec_d == 1) {
				AC = Cal_Ex;
			} else {
				AC = 0;
			}
			if (Dec_d == 0) {
				DC = Cal_Ex;
			} else {
				DC = 0;
			}
			IMC = P_re / (Alt * Alt);
			if (IMC < 18.5) {
				EI_ade = TEE;
			} else {
				EI_ade = EI;
			}
			EB = EI_ade - TEE;
			if (EB >= 0) {
				AP = (EB / Con_C_Kg) * 365;
			} else {
				AP = 0;
			}
			if (EB < 0) {
				DP = (Math.abs(EB / Con_C_Kg)) * 365;

			} else {
				DP = 0;
			}

			// Niveles
			// Auxiliares
			Ed = Ed + Cont;
			T_tra = T_tra + (F_tt * pasoSimu);
			T_cig = T_cig + (F_dia * pasoSimu);
			Tempo = Tempo - (Fs_tempo * pasoSimu);
			P_dejaf = P_dejaf + (F_pdef * pasoSimu);
			// Sector peso
			P = P + (AP * pasoSimu) - (DP * pasoSimu);

			aux2 = aux2 + 1;
			EI = EI + (AC * pasoSimu) - (DC * pasoSimu);

			tiempoI = tiempoI + pasoSimu;
			// System. out. println(tiempoI+" "+"riesgo="+lHTA);

		} // fin while

	}

